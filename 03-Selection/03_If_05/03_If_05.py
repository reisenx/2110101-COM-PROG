# Input a number
number = int(input())

# Check if the number is positive, negative, or zero
if number > 0:
    print("positive")
elif number < 0:
    print("negative")
else:
    print("zero")

# Check if the number is even or odd
if number % 2 == 0:
    print("even")
else:
    print("odd")
