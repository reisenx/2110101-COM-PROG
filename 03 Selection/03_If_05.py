#Input a number as a integer
num = int(input())

#Check if the number is positive, negative or zero
if(num>0):
    print("postive")
elif(num<0):
    print("negative")
elif(num==0):
    print("zero")

#Check if the number is even or odd
if(num%2==0):
    print("even")
elif(num%2!=0):
    print("odd")